function var_check() {
    // console.log(temp);
    if(1) {
        var temp = 1;
        console.log(temp);
    }

    console.log(temp);
}

function let_check() {
    // console.log(temp);
    if(1) {
        let temp = 1;
        console.log(temp);
    }

    console.log(temp);
}

console.log("Var Check");
var_check();
console.log();
console.log("Let Check");
let_check();